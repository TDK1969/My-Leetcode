from typing import *
from collections import *
from leetcode_utils import *
class Solution:
    def numRookCaptures(self, board: List[List[str]]) -> int:
        m, n = len(board), len(board[0])
        for i in range(m):
            for j in range(n):
                if board[i][j] == "R":
                    x, y = i, j
        
        ans = 0
        directions = [[1, 0], [-1, 0], [0, 1], [0, -1]]
        for dx, dy in directions:
            nx, ny = x + dx, y + dy
            while (0 <= nx < m and 0 <= ny < n):
                if board[nx][ny] == "p":
                    ans += 1
                    break
                elif board[nx][ny] == "B":
                    break
                nx, ny = nx + dx, ny + dy
        
        return ans

